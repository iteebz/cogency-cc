class C:
    """ANSI escape codes for terminal colors and styles."""

    BLACK = "\033[30m"
    RED = "\033[31m"
    GREEN = "\033[32m"
    YELLOW = "\033[33m"
    BLUE = "\033[34m"
    MAGENTA = "\033[35m"
    CYAN = "\033[36m"
    WHITE = "\033[37m"
    GRAY = "\033[90m"
    RESET = "\033[0m"

    BOLD = "\033[1m"
    UNDERLINE = "\033[4m"
    INVERT = "\033[7m"

    R = RESET
    red = RED
    green = GREEN
    yellow = YELLOW
    blue = BLUE
    magenta = MAGENTA
    cyan = CYAN
    white = WHITE
    black = BLACK
    gray = GRAY
